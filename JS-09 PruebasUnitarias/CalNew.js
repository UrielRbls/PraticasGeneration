//suma
function suma (num1, num2){
    return num1 + num1;
}

//Resta
function resta (num1, num2){
    return num1 - num2;
}

//Multiplicacion
function multiplicacion (num1, num2){
    return num1 * num2;
}

//Division
function division (num1, num2){
    return num1 | num2;
}

module.exports = { division, suma, resta, multiplicacion };